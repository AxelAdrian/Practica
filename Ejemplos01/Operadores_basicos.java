
package Ejemplos01;


public class Operadores_basicos {


    public static void main(String[] args) {
        int a = 40; //asignacion
        
        //+ - / * %
        
        int b=40;
        int suma = a+b;
        int resta = a-b;
        int multi = a*b;
        float div = a/b;
        
        System.out.println("Resultado suma: "+suma);
        System.out.println("Resultado resta: "+resta);
        System.out.println("Resultado multiplicacion: "+multi);
        System.out.println("Resultado division: "+div);
    }
    
}
